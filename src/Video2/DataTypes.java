package Video2;

public class DataTypes {
    public static void main(String[] args) {
        System.out.println("================================== byte ===========================================");
        byte bMaxValue=Byte.MAX_VALUE;
        byte bMinValue= Byte.MIN_VALUE;
        System.out.println(bMaxValue);
        System.out.println(bMinValue);

        System.out.println("================================== short ===========================================");
        short sMaxValue=Short.MAX_VALUE;
        short sMinValue= Short.MIN_VALUE;
        System.out.println(sMaxValue);
        System.out.println(sMinValue);

        System.out.println("================================ int =============================================");
        int iMaxValue=Integer.MAX_VALUE;
        int iMinValue= Integer.MIN_VALUE;
        System.out.println(iMaxValue);
        System.out.println(iMinValue);

        System.out.println("================================== long ===========================================");
        long lMaxValue = Long.MAX_VALUE;
        long lMinValue = Long.MIN_VALUE;
        System.out.println(lMaxValue);
        System.out.println(lMinValue);

        System.out.println("================================ float =============================================");
        float fMaxValue=Float.MAX_VALUE;
        float fMinValue= Float.MIN_VALUE;
        System.out.println(fMaxValue);
        System.out.println(fMinValue);

        System.out.println("================================== double ===========================================");
        double dMaxValue = Double.MAX_VALUE;
        double dMinValue = Double.MIN_VALUE;
        System.out.println(dMaxValue);
        System.out.println(dMinValue);

        System.out.println("================================== boolean ===========================================");
        boolean b1 = true;
        boolean b2 = false;
        System.out.println(b1);
        System.out.println(b2);

        System.out.println("================================== char ===========================================");
        char c1 = 'a';
        char c2 = '5';
        char c3 = '#';
        System.out.println(c1);
        System.out.println(c2);
        System.out.println(c3);
    }
}
